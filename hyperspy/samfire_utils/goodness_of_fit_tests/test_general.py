import numpy as np


class goodness_test(object):

    _tolerance = None

    @property
    def tolerance(self):
        return self._tolerance

    @tolerance.setter
    def tolerance(self, value):
        if value is None:
            self._tolerance = None
        else:
            self._tolerance = np.abs(value)
